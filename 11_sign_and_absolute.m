a = +6

b = 0

c = -6

d = 3 + 6i

e = [ 10 0 +10 -10 ]


% ##########################     sign function   ###########################

sign(a) %   It will compute if the sign is positive will print the result of 1. 

sign(b) %   If the value is 0 it will print 0. 

sign(c) %   And if the value is minus than 0, it will print -1.

sign(d) %   It will divide the real and imaginary part to the magnitude of the complex number.

sign(e) %   Same operation but for each element in the array (applies the same for a matrix).


% ##########################    absolute function    ##############################

abs(a) % It will compute the same value discarting the sign (being always positive).

abs(b) % If the value is 0, -0 or +0, the absolute result is 0.

abs(c) % The absolute value of -6 is 6.

abs(d) % The absolute of any complex number, will return the magnitude of the complex number.

abs(e) %   Same operation but for each element in the array (applies the same for a matrix).
