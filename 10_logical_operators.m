a = [ 1 5 8 7 0 ]
b = [ 1 1 0 2 3 ]

and(a, b) % If any comparation value position is diferent of 0, it will print 1. If any comparation value position is equal to 0, it will print 0.

a & b % The same of the 'and()' function.

and(a, 0) % Comparate all values in the array with '0'.

and(a, 1) % Comparate all values in the array with '1'.

a | b % The same of the 'or()' function.

or(a, b) % If almost one value diferent to 0 of the comparate position values is 1, it will print 1. If there is only 0 in the two cases it will print 0.

or(a, 0) % Comparate all values in the array with '0'.

not(a) % If there is any number diferent to 0, this negation will be the same 0 as result. In the otherwise if the value is 0, the negation will be 1.

~a % The same of the 'not()' function.
